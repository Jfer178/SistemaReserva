    private void botonPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonPDFActionPerformed
 
    String idCliente = jTextCedula.getText();
    String silla = jTextFieldSILLA.getText();
    String categoria = jTextFieldCategoria.getText();
    String fecha = jTextFieldFECHA.getText();
    String hora = jTextFieldHORA.getText();
    String nroVuelo = jTextFieldNUMVUELO.getText();
    String destino = jTextFieldDestino.getText();
    String origen = jTextFieldORIGEN1.getText();
    
    

    Document documento = new Document();

    // Crear un JFileChooser para elegir el nombre y la ubicación del archivo PDF
    JFileChooser fileChooser = new JFileChooser();
    FileNameExtensionFilter filter = new FileNameExtensionFilter("PDF Documents", "pdf");
    fileChooser.setFileFilter(filter);
    fileChooser.setDialogTitle("Guardar PDF");

    // Mostrar el diálogo para guardar
    int userSelection = fileChooser.showSaveDialog(null);

    if (userSelection == JFileChooser.APPROVE_OPTION) {
        File fileToSave = fileChooser.getSelectedFile();

        // Asegúrate de que el archivo tenga la extensión .pdf
        String filePath = fileToSave.getAbsolutePath();
        if (!filePath.toLowerCase().endsWith(".pdf")) {
            filePath += ".pdf";
        }

        try {
            // Crear el escritor del PDF
            PdfWriter.getInstance(documento, new FileOutputStream(filePath));

            // Abrir el documento
            documento.open();

            // Agregar una imagen (opcional, por ejemplo el logo de la aerolínea)
            Image header = Image.getInstance("C:/ruta/a/tu/imagen/logo.png");
            header.scaleToFit(300, 200);
            header.setAlignment(Chunk.ALIGN_RIGHT);

            // Agregar un título o encabezado
            Paragraph parrafo = new Paragraph();
            parrafo.setAlignment(Paragraph.ALIGN_CENTER);
            parrafo.setFont(FontFactory.getFont("Tahoma", 18, Font.BOLD, BaseColor.DARK_GRAY));
            parrafo.add("BOLETO DE VUELO\n\n");

            // Agregar la fecha y hora de la creación del PDF
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
            String fechaHora = sdf.format(new Date());
            Paragraph fechaParrafo = new Paragraph("Fecha de emisión: " + fechaHora);
            fechaParrafo.setAlignment(Paragraph.ALIGN_RIGHT);

            // Agregar los párrafos al documento
            documento.add(header);
            documento.add(parrafo);
            documento.add(fechaParrafo);
            documento.add(Chunk.NEWLINE);

            // Crear una tabla para los detalles del boleto
            PdfPTable table = new PdfPTable(2);  // Dos columnas: una para el campo y otra para el valor
            table.setWidthPercentage(100);

            // Celdas del encabezado de la tabla
            table.addCell("Campo");
            table.addCell("Valor");

            // Agregar los datos del boleto a la tabla
            table.addCell("ID CLIENTE");
            table.addCell(String.valueOf(idCliente));
            table.addCell("SILLA");
            table.addCell(String.valueOf(silla));
            table.addCell("CATEGORIA");
            table.addCell(categoria);  // Asegúrate de que este campo esté en BoletoModelo
            table.addCell("FECHA");
            table.addCell(fecha);
            table.addCell("HORA");
            table.addCell(hora);
            table.addCell("NRO VUELO");
            table.addCell(nroVuelo);
            table.addCell("DESTINO");
            table.addCell(destino);
            table.addCell("ORIGEN");
            table.addCell(origen);

            // Agregar la tabla al documento
            documento.add(table);

            // Cerrar el documento
            documento.close();

            // Mostrar mensaje de confirmación
            JOptionPane.showMessageDialog(null, "Boleto PDF creado con éxito.");
        } catch (DocumentException | FileNotFoundException e) {
            JOptionPane.showMessageDialog(null, "ERROR AL CREAR PDF: " + e.getMessage());
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "ERROR EN LA IMAGEN: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_botonPDFActionPerformed

