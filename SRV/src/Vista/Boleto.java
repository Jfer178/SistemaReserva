    private void JTBoletoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JTBoletoMouseClicked
        try (Connection con = new conexionbasededatos().conectar()) {
            // Obtén la fila seleccionada de la tabla
            int fila = JTBoleto.getSelectedRow();

            // Asegúrate de que la fila no esté vacía
            if (fila == -1) {
                JOptionPane.showMessageDialog(this, "No se ha seleccionado ninguna fila.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Obtener el IDCLIENTE de la primera columna (suponiendo que IDCLIENTE está en la primera columna)
            String idCliente = JTBoleto.getValueAt(fila, 0).toString();

            // Consulta SQL corregida
            String sql = "SELECT `IDRESERVA`,`IDCLIENTE`, `SILLA`, `CATEGORIA`, `FECHA`, `HORA`, `NRO_VUELO`, `DESTINO`, `ORIGEN` FROM `reserva` WHERE `IDCLIENTE` = ?";

            // Preparar y ejecutar la consulta
            try (PreparedStatement ps = con.prepareStatement(sql)) {
                ps.setString(1, idCliente); // Establecer el valor del parámetro

                try (ResultSet rs = ps.executeQuery()) {
                    // Si la consulta devuelve un resultado
                    if (rs.next()) {
                        // Asignar los valores a los campos de texto correspondientes
                        jLabel4IReserva.setText(String.valueOf(rs.getInt("IDVUELO")));
                        jLabel4IDVUELO1.setText(String.valueOf(rs.getInt("IDVUELO")));
                        jTextCedula.setText(rs.getString("IDCLIENTE"));  // Usar el campo correcto (IDCLIENTE)
                        jTextFieldSILLA.setText(String.valueOf(rs.getInt("SILLA")));
                        jTextFieldCategoria.setText(rs.getString("CATEGORIA"));
                        jTextFieldFECHA.setText(rs.getString("FECHA"));
                        jTextFieldHORA.setText(rs.getString("HORA"));
                        jTextFieldNUMVUELO.setText(rs.getString("NRO_VUELO"));
                        jTextFieldDestino.setText(rs.getString("DESTINO"));
                        jTextFieldORIGEN1.setText(rs.getString("ORIGEN"));
                    } else {
                        // Si no se encuentran resultados, mostrar un mensaje
                        JOptionPane.showMessageDialog(this, "No se encontraron datos para este cliente.", "Información", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
            }
        } catch (SQLException e) {
            // Si ocurre un error, mostrar el mensaje de error
            JOptionPane.showMessageDialog(this, "ERROR AL CARGAR DATOS: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_JTBoletoMouseClicked

